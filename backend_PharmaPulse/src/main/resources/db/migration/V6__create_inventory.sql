ALTER TABLE inventory
    ADD CONSTRAINT FK_INVENTORY_ON_LOCATION FOREIGN KEY (location_id) REFERENCES inventory_location (location_id);

ALTER TABLE truck_inventory
    ADD CONSTRAINT FK_TRUCKINVENTORY_ON_INVENTORY FOREIGN KEY (inventory_id) REFERENCES inventory (inventory_id);

ALTER TABLE warehouse_inventory
    ADD CONSTRAINT FK_WAREHOUSEINVENTORY_ON_INVENTORY FOREIGN KEY (inventory_id) REFERENCES inventory (inventory_id);